abstract public class Animal{
	private int peso;
	private int velocidade;
	
	public int getPeso (){
		return peso;
	}
	
	public int getVelocidade(){
		return velocidade;
	}
	
	public void setPeso(int peso){
		this.peso = peso;
	}
	
	public void setVelocidade(int velocidade){
			this.velocidade=velocidade;
	}
	
}