public class Data {

    private int dia;
    private int mes;

    public Data() {
        dia = 0;
        mes = 0;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getDia(){
        return dia;
    }

    public int getMes() {
        return mes;
    }

}